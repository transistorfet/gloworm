
#ifndef _SYS_SCHED_H
#define _SYS_SCHED_H

#define CLONE_VM		0x000100	// set if the address space is shared between processes
#define CLONE_FS		0x000200	// set if the fs info is shared between processes
#define CLONE_FILES		0x000400	// set if the open files are shared between processes
#define CLONE_THREAD		0x010000	// use the same thread group for the child process

#endif

